    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        if(bedit.getText() == "bedit"){
           txtnim.requestFocus();
           txtnama.requestFocus();
        }else{
            String nama = txtnama.getText();
            String nim = txtnim.getText();
            String kelas = (String) cmbkelas.getSelectedItem();
	    String tugas = txttugas.getText();
            String quiz = txtquiz.getText();
            String uts = txtuts.getText();
            String uas = txtuas.getText();
            
        try{
            Statement statement = (Statement) konek.getConnection().createStatement();
            statement.executeUpdate("UPDATE nilai_mhs SET nim='"+nim+"',"+"nama='"+nama+"',"+"kelas='"+kelas+"',"+"tugas='"+tugas+"',"+"quiz='"+quiz+"',"+"uts='"+uts+"',"+"uas='"+uas+"'"+ "WHERE nim = '"+nim+"' OR nama = '"+nama+"'");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
        }catch (Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal diubah");
        }
        datatable();
        }
        
    }//GEN-LAST:event_beditActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        String nama = txtnama.getText();
        String nim = txtnim.getText();
        
        try{
            Statement statement = (Statement) konek.getConnection().createStatement();
            statement.executeUpdate("DELETE FROM nilai_mhs WHERE nama = '" + nama + "' OR nim ='" + nim + "';");
            JOptionPane.showMessageDialog(null, "Data Berhasil di Hapus");
            txtnama.setText("");
            txtnim.setText("");
            cmbkelas.setSelectedItem("A");
            txttugas.setText("");
            txtquiz.setText("");
            txtuts.setText("");
            txtuas.setText("");
            txtnim.requestFocus();
        }catch (Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal di Hapus");
        }
        datatable();
    }//GEN-LAST:event_bhapusActionPerformed

