    private void datatable(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Nama");
        tbl.addColumn("NIM");
        tbl.addColumn("Kelas");
        tbl.addColumn("Tugas");
        tbl.addColumn("Quiz");
        tbl.addColumn("UTS");
        tbl.addColumn("UAS");
        table.setModel(tbl);
        try{
            Statement statement = (Statement) konek.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM nilai_mhs");
            while (res.next())
            {
                tbl.addRow(new Object[]{
                    res.getString("Nama"),
                    res.getString("NIM"),
                    res.getString("Kelas"),
                    res.getString("Tugas"),
                    res.getString("Quiz"),
                    res.getString("UTS"),
                    res.getString("UAS"),
                });
            }
            table.setModel(tbl);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }

    

    }
