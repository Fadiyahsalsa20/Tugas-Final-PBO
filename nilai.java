    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String nama = txtnama.getText();
        String nim = txtnim.getText();
        String kelas = (String) cmbkelas.getSelectedItem();
        String tugas = txttugas.getText();
        String quiz = txtquiz.getText();
        String uts = txtuts.getText();
        String uas = txtuas.getText();

    try{
        Statement statement = (Statement) konek.getConnection().createStatement();
        statement.executeUpdate("INSERT INTO nilai_mhs VALUES ('" + nama + "','" + nim +"','"+ kelas +"','"+ tugas +"','"+ quiz +"','"+ uts +"','"+ uas +"');");
        statement.close();
        JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");
    }catch (Exception t){
        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
    }
    datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

